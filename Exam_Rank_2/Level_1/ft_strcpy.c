#include <stdio.h>

char	*ft_strcpy(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (s2[i] != '\0')
	{
		s1[i] = s2[i];
		i++;
	}
	s1[i] = '\0';
	return (s1);
}

int	main(void)
{
	char	tab[] = "Je suis une star";
	char	tabo[10];

	printf("%s\n", tabo);
	printf("%s\n", ft_strcpy(tabo, tab));
	return (0);
}
